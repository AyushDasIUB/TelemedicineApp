package Ayush;

/**
 *
 * @author dasay
 */
public class Interpreter extends User{
    protected String email;
    protected String gender;
    protected int phoneNumber;
    protected String dob;

    public Interpreter(String email, String gender, int phoneNumber, String name, int id, String category, String password) {
        super(name, id, category, password);
        this.email = email;
        this.gender = gender;
        this.phoneNumber = phoneNumber;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getGender() {
        return gender;
    }

    public void setGender(String gender) {
        this.gender = gender;
    }

    public int getPhoneNumber() {
        return phoneNumber;
    }

    public void setPhoneNumber(int phoneNumber) {
        this.phoneNumber = phoneNumber;
    }

    public String getDob() {
        return dob;
    }

    public void setDob(String dob) {
        this.dob = dob;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getCategory() {
        return category;
    }

    public void setCategory(String category) {
        this.category = category;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    @Override
    public String toString() {
        return "Interpreter{" + "email=" + email + ", gender=" + gender + ", phoneNumber=" + phoneNumber + ", dob=" + dob + '}';
    }
    
    
    
}
